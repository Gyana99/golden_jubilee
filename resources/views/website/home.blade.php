<script>
    var AJAXURL = "{{ APP_URL }}";
    console.log(AJAXURL);

    var CSRFTOKEN = "{{ csrf_token() }}";





    $(document).ready(function() {
        // About CSM (Employee Photo Grid Random Image choosing and animation)
        /*----------Get Employee Images---------*/
        var IMAGE_LIST = "";
        var TOTAL_EMPLOYEE = 0;
        $.ajax({
            type: "POST",
            url: AJAXURL + "/getEmployeeImage",
            data: {
                _token: CSRFTOKEN
            },
            dataType: "JSON",
            success: function(data) {
                console.log(data);

                IMAGE_LIST = data.images;
                TOTAL_EMPLOYEE = data.total_image;

                var gridCount = TOTAL_EMPLOYEE < 170 ? TOTAL_EMPLOYEE : 170;
                for (var i = 1; i <= gridCount; i++) {
                    $('.img-grid__middle .grid__item--' + i).prepend("<img class='fadein' alt='' id=" + i + " src='" + IMAGE_LIST[i] + "' />");
                };
            }
        })
        /*-------------End------------*/

        function randomImgChange(midItemNo) {
            var gridCount = TOTAL_EMPLOYEE < 170 ? TOTAL_EMPLOYEE : 170;
            for (var i = 1; i <= 1; i++) {
                var totalImgCount = TOTAL_EMPLOYEE;
                var randomNumber = Math.floor((Math.random() * totalImgCount) + 1);
                var randomItem = Math.floor((Math.random() * gridCount) + 1);

                // Get current DOM img ids
                var imgIDsMid = [];
                $('.img-grid__middle .img-grid__item').each(function() {
                    imgIDsMid[i++] = $(this).find("img").attr('id');
                });

                // String array to Number array
                var resultMid = imgIDsMid.map(function(x) {
                    return parseInt(x, 10);
                });

                // Duplicate check
                if (IMAGE_LIST[randomNumber] !== undefined) {

                    if (jQuery.inArray(randomNumber, resultMid) != -1) {
                        // console.log(resultMid);
                        // console.log("rand"+randomNumber);
                    } else if (midItemNo == undefined) {
                        // console.log("undefin")+randomNumber;
                        $('.img-grid__middle .grid__item--' + randomItem).html("<img class='fadein' alt='' id=" + randomNumber + " src='{{ ROOT_URL }}storage/uploads/emp_images/" + IMAGE_LIST[randomNumber] + "' />");
                        //console.log(randomItem+" "+randomNumber+" MID Undefined");
                    } else {
                        // console.log("dom"+resultMid);
                        // console.log("item"+randomNumber);
                        $('.img-grid__middle .grid__item--' + midItemNo).html("<img class='fadein' alt='' id=" + randomNumber + " src='{{ ROOT_URL }}storage/uploads/emp_images/" + IMAGE_LIST[randomNumber] + "' />");
                        //console.log(randomItem+" "+randomNumber+" MID");
                    }
                }
            };
        }

        // Randomly change Image after few sec
        setInterval(function() {
            randomImgChange();
        }, 6000);

        // Change image on clicking on any image
        $(".img-grid__middle .img-grid__item").on("click", function() {
            let cls = $(this).attr("class");
            let currentScope = cls.slice(27, 30);
            randomImgChange(currentScope);
        });
    });
</script>
